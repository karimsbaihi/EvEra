

class Event {

  final int id;
  final String title;
  final String description;
  final String place;
  final String date;
  final String startTime;
  final String endTime;
  final String type;
  final String imageUrl;

  const Event({
    required this.id,
    required this.title,
    required this.description,
    required this.place,
    required this.date,
    required this.startTime,
    required this.endTime,
    required this.type,
    required this.imageUrl
  });


}